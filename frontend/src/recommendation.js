function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function getGiftSuggestion(answers) {
  const catId = answers.category || 'other';
  const styleId = answers.style || '';
  const catMap = giftIdeas[catId] || giftIdeas.other;

  if (catId === 'box' && answers.boxContent) {
    const boxArr = catMap[answers.boxContent] || catMap._default || ['подарочный бокс'];
    return pickRandom(boxArr);
  }

  const styleArr = catMap[styleId];
  if (styleArr) return pickRandom(styleArr);

  const defaultArr = catMap._default;
  if (defaultArr) return pickRandom(defaultArr);

  return 'персональный подарок, подобранный специально для вас';
}

function getPriceFit(price, suggestion) {
  const budgetMap = {
    under1k: 'до 1 000 ₽',
    '1k-3k': 'в пределах 1–3 тыс. ₽',
    '3k-5k': 'в пределах 3–5 тыс. ₽',
    '5k-10k': 'в пределах 5–10 тыс. ₽',
    '10k-20k': 'в пределах 10–20 тыс. ₽',
    '20k+': 'от 20 тыс. ₽',
  };
  return budgetMap[price] || null;
}

export function getRecommendation(answers) {
  const gift = getGiftSuggestion(answers);
  const priceFit = getPriceFit(answers.price);

  let text = gift.charAt(0).toUpperCase() + gift.slice(1);
  if (priceFit) {
    text += ` (${priceFit})`;
  }
  if (!text.endsWith('.')) text += '.';
  return text;
}

